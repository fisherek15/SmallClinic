    private void btnDodajDodChoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajDodChoActionPerformed
        
        cn.zerosSucced();
        
        if(!"".equals(txtNazwaDodCho.getText()) 
                && !"".equals(txtObjawyDodCho.getText()) 
                && !"".equals(txtLeczenieDodCho.getText())) {
            
            String name = txtNazwaDodCho.getText();
            String symptoms = txtObjawyDodCho.getText();
            String cure = txtLeczenieDodCho.getText();

            cn.connect();
            cn.executeUpdating("INSERT INTO choroby "
                    + "(nazwa_choroby, objawy, leczenie)"
                    + " VALUES ('" + name + "','" + symptoms + "','" + cure + "')");
            cn.disconnect();
            
            if(!cn.checkSucced()) {                    
                JOptionPane.showMessageDialog(null, "Jednostka chorobowa zosta≈Ça dodana do bazy.");                    
                txtNazwaDodCho.setText("");
                txtObjawyDodCho.setText("");
                txtLeczenieDodCho.setText("");
                txtNazwaDodCho.setFocusable(true);
            }
        }
    }//GEN-LAST:event_btnDodajDodChoActionPerformed

