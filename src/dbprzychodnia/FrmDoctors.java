    private void btnUsunLekarzaLekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUsunLekarzaLekActionPerformed
        
        if(!"".equals(txtIDLek.getText())) {
            cn.connect();
            String sql = "SELECT * FROM lekarze "
                    + "WHERE id_lekarza = " + Integer.parseInt(txtIDLek.getText());
            cn.executeQuerying(sql);
            int resp = JOptionPane.showConfirmDialog(null, "Czy na pewno chcesz wykonać "
                    + sql + "?");

            if(resp == JOptionPane.YES_OPTION) {
                sql = "DELETE FROM lekarze "
                        + " WHERE id_lekarza = " + Integer.parseInt(txtIDLek.getText());
                cn.executeUpdating(sql);
                cn.disconnect();
                txtIDLek.setText("");
                txtImieLek.setText("");
                txtNazwiskoLek.setText("");
                txtTelefonLek.setText("");
                TableDoctors();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Najpierw wybierz lekarza.");
        }
    }//GEN-LAST:event_btnUsunLekarzaLekActionPerformed

