    public void TableDoctors() {
        
        if(!"".equals(txtWyszukajLek.getText())) {
            lista.setColumnCount(0);
            lista.setRowCount(0);
            cn.connect();
            String sql = "SELECT * FROM lekarze "
                    + "WHERE UPPER(imie) "
                    + "LIKE '%" + txtWyszukajLek.getText().toUpperCase() + "%' " 
                    + " OR UPPER(nazwisko) "
                    + "LIKE '%" + txtWyszukajLek.getText().toUpperCase() + "%' "
                    + " OR id_lekarza "
                    + "LIKE '%" + txtWyszukajLek.getText() + "%' "
                    + " ORDER BY nazwisko";
            
            cn.executeQuerying(sql);
            
            lista.addColumn("ID");
            lista.addColumn("Imie");
            lista.addColumn("Nazwisko");
            lista.addColumn("Telefon");
            try {
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{cn.rs.getString("id_lekarza"), cn.rs.getString("imie"), 
                        cn.rs.getString("nazwisko"), cn.rs.getString("telefon")});
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Błąd podczas przeglądania bazy danych: " + ex);
            }
            cn.disconnect();
            tabelaLek.setModel(lista);            
        } else {
            lista.setColumnCount(0);
            lista.setRowCount(0);
            cn.connect();
            String sql = "SELECT * FROM lekarze ORDER BY nazwisko";
            
            cn.executeQuerying(sql);
            
            lista.addColumn("ID");
            lista.addColumn("Imie");
            lista.addColumn("Nazwisko");
            lista.addColumn("Telefon");
            try {
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{cn.rs.getString("id_lekarza"), cn.rs.getString("imie"), 
                        cn.rs.getString("nazwisko"), cn.rs.getString("telefon")});
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Błąd podczas przeglądania bazy danych: " + ex);
            }
            cn.disconnect();
            tabelaLek.setModel(lista);
        }
    }
    
