    public void TableMedicines() {
        
        if(!"".equals(txtWyszukajLekarstwo.getText())) {
            lista.setColumnCount(0);
            lista.setRowCount(0);
            cn.connect();
            String sql = "SELECT * FROM lekarstwa "
                    + "WHERE UPPER(nazwa_lekarstwa) "
                    + "LIKE '%" + txtWyszukajLekarstwo.getText().toUpperCase() + "%' " 
                    + " OR UPPER(sklad) "
                    + "LIKE '%" + txtWyszukajLekarstwo.getText().toUpperCase() + "%' "
                    + " ORDER BY nazwa_lekarstwa";
            
            cn.executeQuerying(sql);
            
            lista.addColumn("ID");
            lista.addColumn("Nazwa");
            lista.addColumn("Skład");
            lista.addColumn("Przeciwwskazania");
            lista.addColumn("Skutki uboczne");
            try {
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{cn.rs.getString("id_lekarstwa"), cn.rs.getString("nazwa_lekarstwa"), 
                        cn.rs.getString("sklad"), cn.rs.getString("przeciwwskazania"), cn.rs.getString("skutki_uboczne")});
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Błąd podczas przeglądania bazy danych: " + ex);
            }
            cn.disconnect();
            tabelaLekarstw.setModel(lista);            
        } else {
            lista.setColumnCount(0);
            lista.setRowCount(0);
            cn.connect();
            String sql = "SELECT * FROM lekarstwa ORDER BY nazwa_lekarstwa";
            
            cn.executeQuerying(sql);
            
            lista.addColumn("ID");
            lista.addColumn("Nazwa");
            lista.addColumn("Skład");
            lista.addColumn("Przeciwwskazania");
            lista.addColumn("Skutki uboczne");
            try {
                while (cn.rs.next()) {
                    
                    lista.addRow(new String[]{cn.rs.getString("id_lekarstwa"), cn.rs.getString("nazwa_lekarstwa"), 
                        cn.rs.getString("sklad"), cn.rs.getString("przeciwwskazania"), cn.rs.getString("skutki_uboczne")});
                }
            } catch (SQLException ex) {
                
                JOptionPane.showMessageDialog(null, "Błąd podczas przeglądania bazy danych: " + ex);
            }
            cn.disconnect();
            tabelaLekarstw.setModel(lista); 
        }
    }
    
