    private void btnDrukujUlotkeLekarstwaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDrukujUlotkeLekarstwaActionPerformed
        
        com.itextpdf.text.Font f1 = null;
        com.itextpdf.text.Font f2 = null;
        com.itextpdf.text.Font f3 = null;
        try {
            BaseFont bf = BaseFont.createFont(BaseFont.TIMES_ROMAN, BaseFont.CP1250, BaseFont.EMBEDDED);
            f1 = new com.itextpdf.text.Font(bf, 18, Font.BOLD, BaseColor.BLACK);
            f2 = new com.itextpdf.text.Font(bf, 14, Font.PLAIN, BaseColor.BLACK);
            f3 = new com.itextpdf.text.Font(bf, 12, Font.PLAIN, BaseColor.DARK_GRAY);
        } catch (DocumentException ex) {
            Logger.getLogger(FrmVisits.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmVisits.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(!"".equals(txtIDLekarstwa.getText()) && !"".equals(txtNazwaLekarstwa.getText()) 
            && !"".equals(txtSkladLekarstwa.getText()) && !"".equals(txtPrzeciwwskazaniaLekarstwa.getText()) 
            && !"".equals(txtSkutkiUboczneLekarstwa.getText())) {

            String RESULT = "Ulotka.pdf";            

            Document document = new Document();

            try {               
                PdfWriter.getInstance(document, new FileOutputStream(RESULT));        
                document.open();
                String txt = "Ulotka - " + txtNazwaLekarstwa.getText() + " ";
                document.add(new Paragraph(txt, f1));        
                document.add(new Paragraph("-------------------------------------"
                        + "-----------------------------------------------------"
                        + "----------------------------------------", f3));
               
                document.add(new Paragraph("\n------- Sklad", f2));                
                txt = ""+txtSkladLekarstwa.getText()+"";
                document.add(new Paragraph(txt, f3));        
                
                document.add(new Paragraph("\n------- Przeciwwskazania", f2));
                txt = ""+txtPrzeciwwskazaniaLekarstwa.getText()+"";
                document.add(new Paragraph(txt, f3));        
                
                document.add(new Paragraph("\n------- Skutki uboczne", f2));                
                txt = ""+txtSkutkiUboczneLekarstwa.getText()+"";
                document.add(new Paragraph(txt, f3));                    
                
                document.add(new Paragraph("\n"));
                document.add(new Paragraph("-------------------------------------"
                        + "-----------------------------------------------------"
                        + "----------------------------------------", f3));

                document.close();
                
            } catch (DocumentException ex) {
                JOptionPane.showMessageDialog(null, "Błąd pliku. " + ex);
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Plik nie został odnaleziony. Sprawdź ścieżkę pliku. " + ex);
            }

            if (Desktop.isDesktopSupported()) {
                try {
                    File myFile = new File("D:/INFORMATICS/Projects Java/DBPrzychodnia2/Ulotka.pdf");
                    Desktop.getDesktop().open(myFile);
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Błąd podczas uruchamiania pliku. Sprawdź ścieżkę pliku." + ex);
                }
            }
        }
    }//GEN-LAST:event_btnDrukujUlotkeLekarstwaActionPerformed

